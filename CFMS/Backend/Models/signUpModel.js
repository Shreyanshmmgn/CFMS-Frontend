const mongoose = require("mongoose");

const schema = mongoose.Schema;

const signUpTemplate = new schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("myTable", signUpTemplate);
